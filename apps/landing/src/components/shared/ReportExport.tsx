interface Props {
  report: ScanReport;
}

export function generateMarkdown(report: ScanReport): string {
  const lines: string[] = [];
  lines.push(`# Agent Preflight Report — ${report.repoName}`);
  lines.push('');
  lines.push(`**Production Readiness Score:** ${report.score.percentage}%`);
  lines.push(`**Status:** ${report.status}`);
  lines.push(`**Duration:** ${(report.duration / 1000).toFixed(1)}s`);
  lines.push(`**Date:** ${new Date(report.timestamp).toISOString()}`);
  lines.push('');
  lines.push('## Score Breakdown');
  lines.push('');
  lines.push('| Category | Score | Status |');
  lines.push('|----------|-------|--------|');
  for (const cat of report.score.categories) {
    const status = cat.percentage >= 75 ? '✅' : cat.percentage >= 50 ? '⚠️' : '❌';
    lines.push(`| ${cat.label} | ${cat.percentage}% | ${status} |`);
  }
  lines.push('');
  lines.push('## Issues Found');
  lines.push('');
  for (const cat of report.categories) {
    const issues = cat.checks.filter(c => c.status === 'fail' || c.status === 'warn');
    if (issues.length === 0) continue;
    lines.push(`### ${cat.categoryLabel}`);
    lines.push('');
    for (const issue of issues) {
      lines.push(`- **${issue.message}**`);
      if (issue.file) lines.push(`  - File: \`${issue.file}${issue.line ? `:${issue.line}` : ''}\``);
      if (issue.suggestedFix) lines.push(`  - Fix: ${issue.suggestedFix}`);
    }
    lines.push('');
  }
  lines.push('---');
  lines.push('Generated by Agent Preflight');
  return lines.join('\n');
}

