export function ReportExport({ report }: Props) {
  const [format, setFormat] = useState<'markdown' | 'json' | 'html'>('markdown');
  const [copied, setCopied] = useState(false);

  const content = format === 'markdown' ? generateMarkdown(report) :
    format === 'json' ? JSON.stringify(report, null, 2) :
    generateHTML(report);

  const filename = `preflight-${report.repoName.replace(/[^a-zA-Z0-9]/g, '-')}`;

  const handleCopy = useCallback(async () => {
    try {
      await navigator.clipboard.writeText(content);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {}
  }, [content]);

  const handleDownload = useCallback(() => {
    const ext = format === 'markdown' ? 'md' : format === 'json' ? 'json' : 'html';
    const mime = format === 'html' ? 'text/html' : format === 'json' ? 'application/json' : 'text/markdown';
    const blob = new Blob([content], { type: mime });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${filename}.${ext}`;
    a.click();
    URL.revokeObjectURL(url);
  }, [content, format, filename]);

  return (
    <div className="panel p-4 space-y-3 animate-fadeIn">
      <div className="flex items-center gap-2">
        <FileText size={14} className="text-base-400" />
        <span className="text-[12px] font-semibold text-base-300 uppercase tracking-wider">Export Report</span>
      </div>

      {/* Format selector */}
      <div className="flex items-center gap-1 p-0.5 bg-base-950 rounded-md border border-base-800">
        {(['markdown', 'json', 'html'] as const).map((f) => (
          <button
            key={f}
            onClick={() => setFormat(f)}
            className={`flex-1 px-3 py-1.5 rounded text-[11px] font-medium transition-colors ${
              format === f ? 'bg-base-800 text-base-200' : 'text-base-500 hover:text-base-300'
            }`}
          >
            {f.toUpperCase()}
          </button>
        ))}
      </div>

      {/* Preview */}
      <div className="panel !bg-base-950 rounded-md max-h-[200px] overflow-y-auto">
        <pre className="p-3 text-[11px] font-mono text-base-400 leading-relaxed whitespace-pre-wrap">
          <code>{content.slice(0, 500)}{content.length > 500 ? '...' : ''}</code>
        </pre>
      </div>

      {/* Actions */}
      <div className="flex items-center gap-2">
        <button onClick={handleCopy} className="btn-ghost flex-1 justify-center text-[12px] py-2">
          {copied ? <Check size={13} className="text-emerald-400" /> : <Copy size={13} />}
          {copied ? 'Copied' : 'Copy'}
        </button>
        <button onClick={handleDownload} className="btn-ghost flex-1 justify-center text-[12px] py-2">
          <Download size={13} />
          Download
        </button>
      </div>
    </div>
  );
}
